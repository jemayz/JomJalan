class Itinerary {
  final String id;
  final String title;
  final String state;
  final String author;
  final String duration;
  final int spots;
  final int clones;
  final String imageUrl;

  Itinerary({
    required this.id,
    required this.title,
    required this.state,
    required this.author,
    required this.duration,
    required this.spots,
    required this.clones,
    required this.imageUrl,
  });
}
